import turtle
t = turtle.Pen()
t1 = turtle.Pen()
# Rechtecke Zeichnung
def rechtecke():
    t.reset()
    t.forward(200)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward(200)
    t.left(90)
    t.forward(100)

    t.up()
    t.left(90)
    t.forward(50)
    t.left(90)
    t.forward(25)

    t.down()
    t.forward(25)
    t.right(90)
    t.forward(50)
    t.right(90)
    t.forward(25)
    t.right(90)
    t.forward(50)


# Herz Zeichnung
def harz():
    t.reset()
    t.up()
    t.width(4)
    t.goto(0, -100)
    t.down()
    t.left(120)
    t.forward(180)
    for _ in range(180):
        t.right(1)
        t.forward(1)
    t.left(120)
    for _ in range(185):
        t.right(1)
        t.forward(1)
    t.forward(185)
    t.reset()
#Hauser Zeichnung
def haus():
    t.reset()
    t1.reset()
    t.up()
    t1.up()
    t.goto(-200, 0)
    t1.goto(100,0)
    t.down()
    t1.down()
    #Haus
    t.forward(100)
    t1.forward(100)
    t.left(90)
    t1.left(90)
    t.forward(100)
    t1.forward(100)
    t.left(90)
    t1.left(90)
    t.forward(100)
    t1.forward(100)
    t.left(90)
    t1.left(90)
    t.forward(100)
    t1.forward(100)
    #Tür
    t.up()
    t1.up()
    t.goto(-150, 0)
    t1.goto(150,0)
    t.down()
    t1.down()
    t.left(180)
    t1.left(180)
    t.forward(30)
    t1.forward(30)
    t.right(90)
    t1.right(90)
    t.forward(20)
    t1.forward(20)
    t.right(90)
    t1.right(90)
    t.forward(30)
    t1.forward(30)
    #Fenster
    t.up()
    t1.up()
    t.goto(-175, 50)
    t1.goto(125, 50)
    t.down()
    t1.down()
    t.left(180)
    t1.left(180)
    t.forward(25)
    t1.forward(25)
    t.right(90)
    t1.right(90)
    t.forward(25)
    t1.forward(25)
    t.right(90)
    t1.right(90)
    t.forward(25)
    t1.forward(25)
    t.right(90)
    t1.right(90)
    t.forward(25)
    t1.forward(25)
    #Dach
    t.up()
    t1.up()
    t.goto(-200,100)
    t1.goto(100,100)
    t.down()
    t1.down()
    t.goto(-150,150)
    t1.goto(150,150)
    t.goto(-100,100)
    t1.goto(200,100)
# Hauptprogramm
while True:
    print("Gibt eine Nummer")
    print("1. Rechteck")
    print("2. Herz")
    print("3. Häuser")
    print("4. Beenden")

    choice = input("Ihre Auswahl (1-4): ")

    if choice == '1':
        t1.reset()
        t1.hideturtle()
        rechtecke()
    elif choice == '2':
        t1.reset()
        t1.hideturtle()
        harz()
    elif choice == '3':
        haus()
    elif choice == '4':
        break
    else:
        print("Ungültige Nummer")

turtle.done()